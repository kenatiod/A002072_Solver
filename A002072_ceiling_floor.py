A141399 = [0, 1, 2, 3, 5, 8, 9, 14, 15, 20, 24, 35, 80, 125, 224, 384, 440, 539, 714, 1715, 2079, 2400, 3024, 4374, 9800, 12375, 123200, 194480, 633555]

Sqrp = [0] + [isqrt(primorial(i)) for i in range(1, 37)]

for ω in range(1, len(A002072)):
    print(f"{ω=:2} {Sqrp[ω]:40,}", end="  ", flush=True)
    hits = [str(n) for n in A141399 if n > 0 and len(primefactors(n*(n+1))) == ω]
    hit_string = "[" + ", ".join(hits) + "]" if hits else ""
    hit_string += " " * (40 - len(hit_string))
    print(f"{hit_string} {A002072[ω]:35,}", end=" ", flush=True)
    print(f" Span = {A002072[ω]-Sqrp[ω]:40,}")

