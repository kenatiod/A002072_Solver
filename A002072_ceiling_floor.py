"""
Show the relationship of the A002072 bound ceiling to the floor of the primorial
numbers. It produces a output showing the floor that is the square root of the 
primorial at ω, the list of known prime-complete product m values at that ω, and
then the ceiling of the possible span from the A002072(ω) value, and finally
the span between the floor and the ceiling. When the floor comes up and hits
the ceiling, there are no possible prime-complete products.

By Ken Clements, June 25, 2026
"""

